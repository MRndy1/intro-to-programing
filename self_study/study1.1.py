def age_check():
    age = int(input("please enter your age here: \n"))
    if age >= 80:
        print("you're still alive??")
    elif age >= 50:
        print("you are old")
    elif age >= 20:
        print("you're an adult")
    elif age >= 12:
        print("you are a teenager")
    elif age >= 1:
        print("you are a child")
    else:
        print("you are a baby")

age_check()

